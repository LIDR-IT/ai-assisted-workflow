/* ─── TYPE DEFINITIONS ─────────────────────────────── */

export type ArtifactType =
  | 'skill'
  | 'command'
  | 'rule'
  | 'hook'
  | 'mcp'
  | 'validation-script'
  | 'agent'
  | 'doc';

export type ArtifactSource = 'lidr' | 'bmad' | 'anytime';
export type ArtifactCriticality = 'required' | 'recommended' | 'optional';

export interface Artifact {
  id: string;
  name: string;
  type: ArtifactType;
  source?: ArtifactSource;
  criticality?: ArtifactCriticality;
  tier?: string;
  phase?: string;
  phaseNum?: number;
  /** Stage slug inside the unified phase model (context|anytime|analysis|planning|specification|sprint-planning|development|qa|security|release) */
  stage?: string;
  description: string;
  triggers?: string[];
  roles?: string[];
  precondition?: string;
  mcpsUsed?: string[];
  relatedSkills?: string[];
  relatedCommands?: string[];
  docPath?: string;
  argument?: string;
  category?: string;
  model?: string;
  gateContribution?: string;
  automation?: string;
  path?: string;
}

export interface WorkflowSuggestion {
  title: string;
  description: string;
  steps: { artifact: string; type: ArtifactType; action: string }[];
  roles: string[];
  tags: string[];
}

export interface WorkflowSuggestionForSearch {
  id: string;
  name: string;
  description: string;
  steps: string[];
  roles: string[];
  estimatedTime?: string;
}

export interface Category {
  name: string;
  count: number;
  artifacts: Artifact[];
}

export interface ArtifactRegistry {
  skills: Artifact[];
  commands: Artifact[];
  workflows: WorkflowSuggestionForSearch[];
  categories: Category[];
  allArtifacts: Artifact[];
}

